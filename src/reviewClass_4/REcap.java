package reviewClass_4;

public class REcap {

	public static void main(String[] args) {
		int num = 0;
		System.out.println(num);
		num = num + 1;
		System.out.println(num);
		num += 1;
		System.out.println(num);
		num++;
		System.out.println(num);

		num = 0;
		System.out.println(num);
		num = num - 1;
		System.out.println(num);
		num -= 1;
		System.out.println(num);
		num--;
		System.out.println(num);
	}

}
