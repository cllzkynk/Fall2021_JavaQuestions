package class_07;

public class IncrementAndDecrement {

	public static void main(String[] args) {

		int num = 90;
		num = num + 1;

		num += 1;
		System.out.println(num);

		num++; // adding 1
				// increment operator it adds 1 to the variable
				// ONLY ONE
		System.out.println(num);

		int num1 = 100;
		num1 -= 1;// subtract 1
		num1--; // subtract 1 as well

		System.out.println(num1);

		// 100--; compiler error cause -->
		// increment and decrement operators can only be used with variables;

		System.out.println("--------------------------------------------------");

		System.out.println("Hello");
		System.out.println("Hello");
		System.out.println("Hello");
		System.out.println("Hello");
		System.out.println("Hello");

		// Loops will help us to avoid repetitive codes execute same block of codes
		// multiple times

		/*
		 * while do while for enhanced for loop/advanced for loops/
		 */

	}

}
