package Eng_Examples;

public class CompoundOperators {

	public static void main(String[] args) {
		
		int num=100;
		num=num+10;
		System.out.println(num);
		
		
		num=num+200;
		System.out.println(num);
		
		
		
		int myNumber=100;
		myNumber+=10;//110
		System.out.println(myNumber);
		
		
		
		myNumber+=200;//310
		System.out.println(myNumber);
		
		myNumber-=100;//210
		System.out.println(myNumber);
	
	myNumber/=10;//21
	System.out.println(myNumber);
	
	myNumber*=1000;//21000
	System.out.println(myNumber);
	
	myNumber%=100; //0
	System.out.println(myNumber);
	
	
	
	int a =12;
	int b=7;
	double doublVar=a/b;
	System.out.println(doublVar);
	
	
	
	
	
	
	
	
	
	}
	
	
	
	

}
