package Eng_Examples;

public class DataTypesAndVariables {

	public static void main(String[] args) {
		// primitive datatypes
		// numeric

		byte variable1 = 127;// -128 to 127

		short variable2 = 100; // -32768 to 32767

		int anyName = 1000; // big numbers

		long variable3 = 10000; // any bigger number

		// to store floating numbers

		float var1 = 12.99f;

		double var2 = 9.99999;

		// represent yes or no (true or false)
		boolean myVariable = true;
		boolean anotherVariable = false;

		// to represent single character

		char myBox = 'C';

		char myBox2 = '7';

		System.out.println(myVariable);

	}

}
