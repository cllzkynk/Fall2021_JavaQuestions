public class PrintingPatternsLongWay {

	public static void main(String[] args) {

		for (int i = 1; i < +5; i++) {
			System.out.print("*");
		}
		System.out.println("*");

		for (int i = 1; i < +5; i++) {
			System.out.print("*");
		}
		System.out.println("*");
		for (int i = 1; i < +5; i++) {
			System.out.print("*");

		}
		System.out.println("*");
	}
}