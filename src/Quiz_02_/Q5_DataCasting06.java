package Quiz.Quiz_02;

public class Q5_DataCasting06 {

	public static void main(String[] args) {
		// Soru 2 ) int veri turunde bir degisken olusturun ve adim adim narrowing yapin ve yazdirin
		
		int sayi1=28;
		System.out.println("int degisken ile : " + sayi1);
		
		short sayi2=(short) sayi1;
		System.out.println("short degisken ile : " + sayi2);

		byte sayi3= (byte) sayi1;
		System.out.println("byte degisken ile : " + sayi3);
		
	}

}
