package InterviewSorulari;

public class Java_05_QuotientAndRemainder {

	public static void main(String[] args) {
		
		int dividend = 30; // dividend--bolunen
		int division = 4; // division--bolum
		
		
		// quotient--bolum
		int quotient = dividend / division;
		System.out.println(quotient); // 7

		// remainder--kalan
		int remainder = dividend % division;
		System.out.println(remainder); // 2
		
	}

}
